package net.zjcclims.constant;

import java.util.Calendar;

public class PilotProjectSummary {

	private String labroomNumber;
	private String labroomName;
	private String projectName;
	private String experimenterType;
	private String majorFit;
	private String courseName;
	private String experimentalRequire;
	private String experimentType;
	
	private int experimentalHours;
	
	public String getLabroomNumber() {
		return labroomNumber;
	}
	public void setLabroomNumber(String labroomNumber) {
		this.labroomNumber = labroomNumber;
	}
	public String getLabroomName() {
		return labroomName;
	}
	public void setLabroomName(String labroomName) {
		this.labroomName = labroomName;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getExperimenterType() {
		return experimenterType;
	}
	public void setExperimenterType(String experimenterType) {
		this.experimenterType = experimenterType;
	}
	public String getMajorFit() {
		return majorFit;
	}
	public void setMajorFit(String majorFit) {
		this.majorFit = majorFit;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getExperimentalRequire() {
		return experimentalRequire;
	}
	public void setExperimentalRequire(String experimentalRequire) {
		this.experimentalRequire = experimentalRequire;
	}
	public String getExperimentType() {
		return experimentType;
	}
	public void setExperimentType(String experimentType) {
		this.experimentType = experimentType;
	}
	public int getExperimentalHours() {
		return experimentalHours;
	}
	public void setExperimentalHours(int experimentalHours) {
		this.experimentalHours = experimentalHours;
	}
}
