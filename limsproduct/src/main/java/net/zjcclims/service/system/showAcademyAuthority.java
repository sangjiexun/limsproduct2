package net.zjcclims.service.system;

public class showAcademyAuthority {

	private String authorityName;
	private String authorityCname;
	private int authorityNumber;
	private int authorityId;
	private int resourcesNumber;

	public int getAuthorityId() {
		return authorityId;
	}
	public void setAuthorityId(int authorityId) {
		this.authorityId = authorityId;
	}
	public String getAuthorityName() {
		return authorityName;
	}
	public void setAuthorityName(String authorityName) {
		this.authorityName = authorityName;
	}
	public String getAuthorityCname() {
		return authorityCname;
	}
	public void setAuthorityCname(String authorityCname) {
		this.authorityCname = authorityCname;
	}
	public int getAuthorityNumber() {
		return authorityNumber;
	}
	public void setAuthorityNumber(int authorityNumber) {
		this.authorityNumber = authorityNumber;
	}
	public int getResourcesNumber() {
		return resourcesNumber;
	}
	public void setResourcesNumber(int resourcesNumber) {
		this.resourcesNumber = resourcesNumber;
	}
}
